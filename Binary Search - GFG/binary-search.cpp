class Solution {
  public:
    int binarysearch(int arr[], int n, int k) {
        // code here
        int f=0;
        int l=n-1;
        
        while(f<=l){
          int   mid=f+(l-f)/2;
            if(arr[mid]==k){
                return mid;
            }
             else if(k<arr[mid]){
                 l=mid-1;
             }
             else 
             f=mid+1;
        }
        return -1;
    }
};

